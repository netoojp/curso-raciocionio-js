//pedir p usuário digitar um número
//converter em inteiro - usa o parseInt

var n1 = prompt("digite um valor");
n1 = parseInt(n1);

var n2 = prompt("digite outro valor");
n2 = parseInt(n2);

//mostra subtracção
alert("a subtração dos valores é: " + (n1 - n2));

//multiplicação
alert("a multiplicação dos valores é: " + (n1 * n2));

//divisão
alert("a divisão dos valores é: " + (n1 / n2));

//soma
alert("a soma dos valores é: " + (n1 + n2));


